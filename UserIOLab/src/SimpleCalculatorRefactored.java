public class SimpleCalculatorRefactored {

    public static double divide   (int a , int b){
        return a/b;
    }

    public static double add   (int a , int b){
        return (int)(a+b);
    }

    public static double multiply  (int a , int b){
        return a*b;
    }

    public static double subtract  (int a , int b){
        return a-b;
    }
}
